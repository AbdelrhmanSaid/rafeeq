import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import surahs from '../src/features/quran/data/surahs.js'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)

const API_BASE = 'https://api.quran.com/api/v4'
const OUT_DIR = path.join(__dirname, '../public/data/quran')
const PAGES_INDEX = path.join(__dirname, '../src/features/quran/data/surahPages.js')
const SLEEP_MS = 150

fs.mkdirSync(OUT_DIR, { recursive: true })

const sleep = (ms) => new Promise((resolve) => setTimeout(resolve, ms))

async function fetchJson(url) {
  for (let attempt = 1; attempt <= 3; attempt++) {
    try {
      const response = await fetch(url)
      if (!response.ok) throw new Error(`HTTP ${response.status}`)
      return await response.json()
    } catch (error) {
      if (attempt === 3) throw new Error(`${url}: ${error.message}`)
      await sleep(1000 * attempt)
    }
  }
}

async function fetchSurahVerses(surahId) {
  const verses = []
  let page = 1
  while (page) {
    const url =
      `${API_BASE}/verses/by_chapter/${surahId}?words=true` +
      `&word_fields=code_v1,v1_page,line_number&fields=text_uthmani,sajdah_number` +
      `&per_page=50&page=${page}`
    const data = await fetchJson(url)
    verses.push(...data.verses)
    page = data.pagination.next_page
    await sleep(SLEEP_MS)
  }
  return verses
}

function transformVerse(verse) {
  const words = verse.words
    .filter((word) => word.code_v1)
    .map((word) => ({
      c: word.code_v1,
      p: word.v1_page,
      l: word.line_number,
      ...(word.char_type_name !== 'word' ? { t: word.char_type_name } : {}),
    }))

  return {
    number: verse.id,
    numberInSurah: verse.verse_number,
    text: verse.text_uthmani.trim(),
    page: words[0]?.p ?? null,
    sajda: verse.sajdah_number ?? null,
    words,
  }
}

const surahPages = {}

for (const surah of surahs) {
  const verses = await fetchSurahVerses(surah.id)
  const ayahs = verses.map(transformVerse)
  const pages = [...new Set(ayahs.flatMap((ayah) => ayah.words.map((word) => word.p)))].sort(
    (a, b) => a - b
  )

  if (ayahs.length !== surah.numberOfAyahs) {
    console.error(`Surah ${surah.id}: expected ${surah.numberOfAyahs} ayahs, got ${ayahs.length}`)
    process.exit(1)
  }

  const payload = {
    id: surah.id,
    name: surah.name,
    revelationType: surah.isMeccan ? 'Meccan' : 'Medinan',
    numberOfAyahs: surah.numberOfAyahs,
    pages,
    ayahs,
  }

  surahPages[surah.id] = [pages[0], pages[pages.length - 1]]
  fs.writeFileSync(path.join(OUT_DIR, `${surah.id}.json`), JSON.stringify(payload))
  console.log(`Surah ${surah.id}: ${ayahs.length} ayahs, pages ${pages[0]}-${pages[pages.length - 1]}`)
}

const pagesIndex = `// Generated by scripts/generate-quran-data.js — [firstPage, lastPage] per surah (QCF V1 / Madani layout)
export default ${JSON.stringify(surahPages, null, 2)}
`
fs.writeFileSync(PAGES_INDEX, pagesIndex)
console.log(`Wrote ${Object.keys(surahPages).length} entries to surahPages.js`)
